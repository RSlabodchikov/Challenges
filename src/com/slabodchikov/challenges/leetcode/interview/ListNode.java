package com.slabodchikov.challenges.leetcode.interview;

/**
 * @author Roman Slabodchikov on 1/24/2023
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
